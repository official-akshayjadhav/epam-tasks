public class Calculator{

	// method to calculate addition of numbers and return result
	public double addition(double no1, double no2){
		return no1 + no2;
	}


	// method to calculate subtraction of numbers and return result
        public double subtraction(double no1, double no2){
                return no1 - no2;
        }

	// method to calculate multiplication of numbers and return result
        public double multiplication(double no1, double no2){
                return no1 * no2;
        }

	// method to calculate division of numbers and return result
        public double division(double no1, double no2){
                return no1 / no2;
        }
}

